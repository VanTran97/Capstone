class LineChartTimeAxis extends Component {
  constructor(props) {
    super(props);
    this.state = {
      // Here timeseriesDs is the configuration object which we will pass as a prop to our ReactFC component.
      timeseriesDs: {
        type: "timeseries",
        renderAt: "container",
        width: "800",
        height: "600",
        dataSource: {
          caption: {
            text: "Bitcoin Price Chart"
          },
          series: "Type",
          yaxis: [
            {
              plot: [
                {
                  value: "Bitcoin",
                  type: "line",
                  style: {
                    plot: {
                      // "stroke-dasharray": "5 2"
                    }
                  }
                },
                {
                  value: "test",
                  type: "line",
                  style: {
                    plot: {}
                  }
                }
              ]
            }
          ],
          chart: {}
        }
      }
    };

    // In this method we will create our DataStore and using that we will create a custom DataTable which takes two
    // parameters, one is data another is schema. Check the method definition to get more info.
    this.createDataTable = this.createDataTable.bind(this);
  }

  createDataTable() {
    Promise.all([dataFetch, schemaFetch]).then(res => {
      const data = res[0];
      const schema = res[1];
      // First we are creating a DataStore
      const fusionDataStore = new FusionCharts.DataStore();
      // After that we are creating a DataTable by passing our data and schema as arguments
      const fusionTable = fusionDataStore.createDataTable(data, schema);
      // Afet that we simply mutated our timeseries datasource by attaching the above
      // DataTaeble into its data property.
      const timeseriesDs = Object.assign({}, this.state.timeseriesDs);
      timeseriesDs.dataSource.data = fusionTable;
      this.setState({
        timeseriesDs
      });
    });
  }

  // We are creating the DataTable immidietly after the component is mounted
  componentDidMount() {
    this.createDataTable();
  }

  render() {
    return (
      <div className="App">
        <Header />
        <ReactFC {...this.state.timeseriesDs} />
      </div>
    );
  }
}

export default LineChartTimeAxis;
